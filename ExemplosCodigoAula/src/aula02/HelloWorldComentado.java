package aula02;

/* Comentarios com mais de uma linha sao feitos assim
 * Evite usar acentos no seu programa
 * Blocos de instrucoes sao iniciados com { e finalizados com }
 * Nomes de classes iniciam com letra maiuscula
 */

public class HelloWorldComentado {
	//O metodo main e responsavel pela execucao
	public static void main(String[] args) {
		/* Strings precisam vir entre aspas
		 * Nomes de variaveis iniciam com letra minuscula
		 * Java e case sensitive, ou seja, str e diferente de sTR
		 * O operador de atribuicao e =, igual em C
		 */
		String str = ("Hello World");
		System.out.println(str);
		// Uma instrucao deve ser encerrada com ;
	}
}